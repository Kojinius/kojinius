/**
 * textarea のカーソル位置から「今カーソルが乗っている単語」を取り出す。
 * 単語境界は英数字 / -_ / Markdown 記号（# * _ ` [ ! > | -）。
 *
 * @param value     textarea.value
 * @param caretPos  textarea.selectionStart
 * @returns 単語文字列、なければ null
 */
export function getCaretWord(value: string, caretPos: number): string | null {
  if (caretPos < 0 || caretPos > value.length) return null;
  // Markdown 記号（# * _ ` [ ! > | -）も「単語」として拾う
  const isWordChar = (c: string) =>
    /[a-zA-Z0-9_\-]/.test(c) || '#*_`[!>|'.includes(c);

  let start = caretPos;
  let end = caretPos;
  while (start > 0 && isWordChar(value[start - 1])) start--;
  while (end < value.length && isWordChar(value[end])) end++;
  if (start === end) return null;
  const word = value.slice(start, end);
  if (word.length > 30) return null; // 長すぎる単語は無視
  return word;
}

/**
 * 言語と単語から Lesson を取得。
 * tsx / jsx / typescript は javascript の辞書を fallback として使う。
 *
 * 2026-05-10 09:00:00 claude-opus-4-7[1m] — 大小文字どちらでもヒット
 * 2026-05-10 11:30:00 claude-opus-4-7[1m] セッションターン数：68 — fix:
 *   case insensitive な言語（SQL / VB / VBA / Excel / HTML）は事前計算 map で
 *   PascalCase 等の任意ケースキーも引けるように。表示時のキーワードは辞書の正規ケース。
 */
const CASE_INSENSITIVE_LANGS = new Set(['sql', 'visual-basic', 'excel-formula', 'markup']);

// case insensitive 言語の小文字キー → { 元キー, lesson } マップ（モジュール初期化時に 1 度だけ計算）
const LESSONS_LOWER: Record<string, Record<string, { key: string; lesson: Lesson }>> = (() => {
  const result: Record<string, Record<string, { key: string; lesson: Lesson }>> = {};
  for (const lang of CASE_INSENSITIVE_LANGS) {
    const dict = LESSONS[lang];
    if (!dict) continue;
    result[lang] = {};
    for (const k of Object.keys(dict)) {
      result[lang][k.toLowerCase()] = { key: k, lesson: dict[k] };
    }
  }
  return result;
})();

export function findLesson(lang: string, word: string | null): { keyword: string; lesson: Lesson } | null {
  if (!word) return null;
  // vba は visual-basic の辞書を使う（同じ grammar 系統、どちらも case insensitive）
  const langKey =
    lang === 'tsx' || lang === 'jsx' || lang === 'typescript' ? 'javascript' :
    lang === 'vba' ? 'visual-basic' : lang;
  const dict = LESSONS[langKey];
  if (!dict) return null;

  // case insensitive 言語: 事前計算 map で任意ケースをヒット
  if (CASE_INSENSITIVE_LANGS.has(langKey)) {
    const entry = LESSONS_LOWER[langKey]?.[word.toLowerCase()];
    if (entry) return { keyword: entry.key, lesson: entry.lesson };
  } else {
    // case sensitive 言語: 完全一致 → 小文字 → 大文字
    if (dict[word]) return { keyword: word, lesson: dict[word] };
    const lower = word.toLowerCase();
    if (dict[lower]) return { keyword: word, lesson: dict[lower] };
    const upper = word.toUpperCase();
    if (dict[upper]) return { keyword: word, lesson: dict[upper] };
  }

  // markdown の --- 部分一致
  if (langKey === 'markdown' && word.startsWith('---') && dict['---']) {
    return { keyword: '---', lesson: dict['---'] };
  }
  return null;
}

/**
 * Markdown のカーソル位置から「行頭マーカー」を抽出する fallback。
 * getCaretWord が拾えない `1.` や ` ``` ` のような複合記号を補助する。
 *
 * @returns マーカー文字列（'1.' / '```' / '#' / '-' / '|' / '>'）or null
 */
export function getMarkdownLineMarker(value: string, caretPos: number): string | null {
  if (caretPos < 0 || caretPos > value.length) return null;
  const lineStart = value.lastIndexOf('\n', caretPos - 1) + 1;
  // 行頭からカーソルまでが「マーカー部分」を含むか判定
  const before = value.slice(lineStart, caretPos);
  // 行末（次の改行）まで含めた行全体も使う
  const lineEnd = value.indexOf('\n', caretPos);
  const fullLine = value.slice(lineStart, lineEnd === -1 ? value.length : lineEnd);

  // 番号付きリスト: ^\s*\d+\. （カーソルがマーカー領域内 or マーカー直後）
  if (/^\s*\d+\.\s?/.test(fullLine)) {
    const m = fullLine.match(/^\s*\d+\./);
    if (m && before.length <= m[0].length + 1) return '1.';
  }
  // コードブロック: ```
  if (/^\s*```/.test(fullLine)) {
    if (before.replace(/^\s*/, '').length <= 5) return '```';
  }
  // テーブル区切り行: |---|---|
  if (/^\s*\|/.test(fullLine) && /\|/.test(before)) return '|';
  return null;
}
